package resistorcolorduo

import "strings"

var Colors = map[string]int{
	"black":  0,
	"brown":  1,
	"red":    2,
	"orange": 3,
	"yellow": 4,
	"green":  5,
	"blue":   6,
	"violet": 7,
	"grey":   8,
	"white":  9,
}

// Value should return the resistance value of a resistor with a given colors.
func Value(colors []string) int {
	return Colors[strings.ToLower(colors[0])]*10 + Colors[strings.ToLower(colors[1])]
}
